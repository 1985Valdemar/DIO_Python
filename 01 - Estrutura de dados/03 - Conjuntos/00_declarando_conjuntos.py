# set = elimina item duplicados
numeros = set([1, 2, 3, 1, 3, 4])
print(numeros)  # {1, 2, 3, 4}

# RETIRA LETRA REPETIDA
letras = set("abacaxi")
print(letras)  # {"b", "a", "c", "x", "i"}

# RETIRA NOME REPETIDO
carros = set(("palio", "gol", "celta", "palio"))
print(carros)  # {"gol", "celta", "palio"}
